[int] $numero1 = Read-Host "Introduce numero 1: "
[int] $numero2 = Read-Host "Introduce numero 2: "

#Suma
$operacion_suma = $numero1 + $numero2
Write-Host = "Resultado Suma: " $operacion_suma
#Resta
$operacion_resta = $numero1 - $numero2
Write-Host = "Resultado Resta: " $operacion_resta
#Multiplicacion
$operacion_multiplicacion = $numero1 * $numero2
Write-Host = "Resultado Multiplicacion: " $operacion_multiplicacion
#Division
$operacion_division = $numero1 / $numero2
Write-Host = "Resultado Division: " $operacion_division